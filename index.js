// sums up players cards values
function sumValPlayer(myCards){
    let sumPlayer = 0;
       myCards.forEach(myCards => {
        sumPlayer += (myCards.value);
       });
       totalSum = sumPlayer;
       playerSumElement.textContent = "Your current count is: " + (totalSum) + "!";
      
       
       if (totalSum === 21){
        endGame(false)
       }
       
       
       if (totalSum > 21) {
        endGame(true);
        
        
    }

}


